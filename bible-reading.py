def get_today_plan():
    """获取今天的读经计划"""
    # 使用北京时间 (UTC+8)
    from datetime import timezone, timedelta
    beijing_tz = timezone(timedelta(hours=8))
    today = datetime.now(beijing_tz).replace(hour=0, minute=0, second=0, microsecond=0)
    days_passed = (today - START_DATE).days + 1
    
    if days_passed < 1:
        return None, "读经计划尚未开始，请等待起始日期。"
    
    if days_passed > len(READING_PLAN):
        return None, "🎉 恭喜！你已经完成全部读经计划！"
    
    day_num, title, chapters = READING_PLAN[days_passed - 1]
    return day_num, title, chapters


def send_wechat_message(day_num, title, chapters):
    """通过 Server酱 发送微信消息"""
    
    # 构建消息标题
    title_msg = f"📖 每日读经 | 第{day_num}天"
    
    # 构建消息内容（Markdown格式）
    from datetime import timezone, timedelta
    beijing_tz = timezone(timedelta(hours=8))
    today = datetime.now(beijing_tz).strftime("%Y年%m月%d日")
    
    desp = f"""## {title_msg}

> 📅 日期：{today}

---

### 📚 今日读经内容

**{title}**

---

### 📝 章节明细

{chapters}

---

### 💡 读经提示

1. 建议安静环境，用心阅读
2. 可配合笔记记录感动
3. 读后可默想思考

---

*🙏 愿神的话语成为你脚前的灯，路上的光*
*— 诗篇 119:105*
"""
    
    # 发送请求
    data = {
        "title": title_msg,
        "desp": desp
    }
    
    try:
        response = requests.post(SERVER_URL, data=data, timeout=10)
        result = response.json()
        
        if result.get("code") == 0:
            print(f"✅ 推送成功！第{day_num}天读经计划已发送到微信")
            return True
        else:
            print(f"❌ 推送失败：{result.get('message', '未知错误')}")
            return False
            
    except Exception as e:
        print(f"❌ 请求异常：{str(e)}")
        return False


def main():
    """主函数"""
    print("=" * 50)
    print("📖 每日读经定时推送脚本 - 云端版")
    print("=" * 50)
    
    if not SENDKEY:
        print("❌ 错误：未设置 SENDKEY 环境变量")
        exit(1)
    
    result = get_today_plan()
    
    if len(result) == 2:
        day_num, message = result
        print(f"ℹ️  {message}")
        # 发送通知消息
        send_wechat_message(0, "读经计划通知", message)
    else:
        day_num, title, chapters = result
        print(f"📅 今天是读经第 {day_num} 天")
        print(f"📖 内容：{title}")
        print(f"📝 章节：\n{chapters}")
        print("-" * 50)
        
        # 发送到微信
        send_wechat_message(day_num, title, chapters)
    
    print("=" * 50)


if __name__ == "__main__":
    main()
